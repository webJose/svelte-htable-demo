<p>
    As you learned in the previous example, a sub-table's caption contain up to two pieces of information, and you can 
    control where these appear as well (either left or right).  This short example brings the previous example's table 
    and applies some CSS to the sub-tables' captions.
</p>
<p>
    In this example, text in the caption is bold.  This is done easily with the <code>table.sub > caption</code> CSS 
    selector.
</p>
<p>
    Targetting the individual parts to give them the borders is almost as easy:  The left content is inside a 
    <code>&lt;span&gt;</code> element marked with the <code>cpt-l</code> CSS class; the right content is inside a 
    <code>&lt;span&gt;</code> element marked with the <code>cpt-r</code> CSS class.
</p>
